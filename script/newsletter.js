function showMessage1() {
    const clickButton1 = document.getElementById("clickButton1");
    const messageBox1 = document.getElementById("messageBox1");
    const hideInput1 = document.getElementById("hideInput1");

    // Hide the button
    clickButton1.style.display = "none";
    hideInput1.style.display = "none";

    // Display the message box
    messageBox1.style.display = "block";
}

